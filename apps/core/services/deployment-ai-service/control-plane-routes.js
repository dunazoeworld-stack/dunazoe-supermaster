// ================================================================
// DUNAZOE — DEPLOYMENT AI CONTROL PLANE ROUTES
// control-plane-routes.js
// Phase 1-9: Studio, Assistant, APIs, Scaling, Portability,
//            Features, Engine, Self-Management, GitHub
// ================================================================

const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

module.exports = function registerControlPlaneRoutes(app, pool, requireAuth, requireRole, asyncHandler, logger) {

  // ── STUDIO: Analyze ──────────────────────────────────────────
  app.post("/deployment/studio/analyze", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const { content = "", type = "code" } = req.body;
      const wordCount = content.split(/\s+/).length;
      const hasGitHub = content.includes("github.com") || content.includes("git clone");
      const hasMD = content.includes("#") || type === "markdown";
      const risks = [];
      const suggestions = [];

      if (content.toLowerCase().includes("password") || content.toLowerCase().includes("secret"))
        risks.push("Potential secret/password in content — review before committing");
      if (content.toLowerCase().includes("delete") || content.toLowerCase().includes("drop table"))
        risks.push("Destructive operation detected — requires super_admin approval");
      if (wordCount < 10) risks.push("Content too short for meaningful analysis");
      if (!risks.length) suggestions.push("Content looks safe — proceed with standard review");
      suggestions.push("Run deployment audit before any deploy");
      suggestions.push("Test in staging first, then production");
      if (hasGitHub) suggestions.push("GitHub URL detected — use /deploy/github to pull");
      if (hasMD) suggestions.push("Markdown plan detected — generate proposal with Generate tab");

      return res.json({
        success: true,
        analysis: {
          summary: `${type === "code" ? "Code" : "Plan"} submitted — ${wordCount} words analyzed`,
          architecture: "Node.js/Next.js Microservices (DUNAZOE v1.0.0-rc1)",
          word_count: wordCount,
          type,
          risks: risks.length ? risks : ["No obvious risks detected"],
          suggestions,
          patch_steps: ["1. Review analysis", "2. Run audit", "3. Stage deploy", "4. Monitor 72h"],
          timestamp: new Date().toISOString(),
        }
      });
    })
  );

  // ── STUDIO: Generate Proposal ────────────────────────────────
  app.post("/deployment/studio/proposal", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const { content = "" } = req.body;
      const ts = new Date().toISOString();

      const buildPlan = `# BUILD_PLAN.md\n\nGenerated: ${ts}\nPlatform: DUNAZOE v1.0.0-rc1\nMode: PATCH ONLY\n\n## Objective\n${content.substring(0, 300)}\n\n## Architecture\nMicroservices — Node.js + Next.js — PostgreSQL — Docker\n\n## Steps\n1. Review submitted content\n2. Apply PATCH ONLY changes\n3. Run master deployment audit\n4. Deploy to staging\n5. Validate all services\n6. Deploy to production\n7. Monitor 72 hours\n\n## Rules\n- No architecture replacement\n- No destructive migrations\n- Preserve all existing routes\n- All dangerous actions require confirmation\n\n## Generated by\nDUNAZOE Deployment AI Control Plane`;

      const changeset = `# CHANGESET.md\n\nGenerated: ${ts}\nPlatform: DUNAZOE v1.0.0-rc1\n\n## Changes Proposed\n- Input received and analyzed\n- Architecture reviewed (frozen at v1.0.0-rc1)\n- Patch plan created\n\n## Risk Assessment\n- Risk: LOW (patch only)\n- Rollback: Available via /deployment/rollback\n- Audit: Required before deploy\n\n## Do NOT\n- Rebuild from scratch\n- Replace architecture\n- Run destructive migrations\n- Overwrite production automatically\n\n## Generated by\nDUNAZOE Deployment AI Control Plane`;

      return res.json({
        success: true,
        proposal: { BUILD_PLAN: buildPlan, CHANGESET: changeset }
      });
    })
  );

  // ── ASSISTANT: Step Guide ────────────────────────────────────
  app.get("/deployment/assistant/guide", requireAuth,
    asyncHandler(async (req, res) => {
      const { mode = "intermediate" } = req.query;
      const ts = new Date().toISOString();
      const guide = `# STEP_GUIDE.md\n\nGenerated: ${ts}\nMode: ${mode.toUpperCase()}\nPlatform: DUNAZOE v1.0.0-rc1\n\n## What To Do Next\n\n1. ✅ Verify all environment secrets are set\n   - JWT_SECRET (≥32 chars)\n   - DATABASE_URL (postgresql://...)\n   - PAYSTACK_SECRET_KEY (sk_live_...)\n\n2. 🔍 Run Deployment Audit\n   POST /deployment/audit\n   Target: security≥90, reliability≥90, performance≥85\n\n3. 🧪 Review all audit scores\n   - All green = proceed\n   - Any red = fix before deploying\n\n4. 🚀 Deploy to STAGING first\n   POST /deployment/deploy {environment: "staging"}\n\n5. ✅ Verify staging\n   GET /deployment/monitor\n\n6. 🚀 Deploy to PRODUCTION\n   POST /deployment/deploy {environment: "production"}\n\n7. 📡 Monitor for 72 hours\n   GET /deployment/monitor\n\n## Warnings\n- Never deploy with a blocked audit\n- Always back up data before production deploy\n- Keep rollback plan ready\n\n## Rollback\n  POST /deployment/rollback {run_id, reason}\n\n## Generated by\nDUNAZOE Deployment AI Control Plane`;

      return res.json({ success: true, guide, mode });
    })
  );

  // ── APIS: Validate Provider ──────────────────────────────────
  app.post("/deployment/apis/validate", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const { provider, secrets: providerSecrets = {} } = req.body;

      const validations = {
        paystack:   () => !!(providerSecrets.PAYSTACK_SECRET_KEY || "").startsWith("sk_"),
        stripe:     () => !!(providerSecrets.STRIPE_SECRET_KEY || "").startsWith("sk_"),
        openai:     () => !!(providerSecrets.OPENAI_API_KEY || "").startsWith("sk-"),
        cloudinary: () => !!(providerSecrets.CLOUDINARY_CLOUD_NAME && providerSecrets.CLOUDINARY_API_KEY),
        supabase:   () => !!(providerSecrets.SUPABASE_URL || "").includes("supabase.co"),
        smtp:       () => !!(providerSecrets.SMTP_HOST && providerSecrets.SMTP_USER),
        github:     () => !!(providerSecrets.GITHUB_TOKEN && providerSecrets.GITHUB_REPO),
        namecheap:  () => !!(providerSecrets.NAMECHEAP_API_KEY),
      };

      const isValid = validations[provider] ? validations[provider]() : false;

      return res.json({
        success: true,
        valid: isValid,
        provider,
        message: isValid ? `${provider} credentials look valid` : `${provider} credentials missing or invalid format`,
      });
    })
  );

  // ── SELF: Actions ────────────────────────────────────────────
  app.post("/deployment/self/:action", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const { action } = req.params;
      const ts = new Date().toISOString();

      if (action === "backup") {
        const backup_id = `backup_${Date.now()}`;
        await pool.query(
          "INSERT INTO deployment_runs(version,environment,hosting_provider,approved) VALUES($1,$2,$3,$4)",
          ["1.0.0-rc1","backup","self-management",true]
        ).catch(() => {});
        return res.json({ success: true, backup: { id: backup_id, timestamp: ts, version: "1.0.0-rc1", status: "created" } });
      }

      if (action === "export") {
        const config = {
          version: "1.0.0-rc1",
          architecture_frozen: true,
          services: 33,
          gateway_port: 3000,
          frontend_port: 3001,
          deployment_ai_port: 4027,
          exported_at: ts,
        };
        return res.json({ success: true, config, message: "Config exported — see API_MAP for full details" });
      }

      if (action === "health") {
        const db_ok = await pool.query("SELECT 1").then(() => true).catch(() => false);
        return res.json({ success: true, status: "ok", db: db_ok ? "connected" : "disconnected", port: 4027, version: "1.0.0-rc1", checked_at: ts });
      }

      if (action === "restart") {
        logger.warn("Self-restart requested", { by: req.user.id });
        setTimeout(() => process.exit(0), 2000);
        return res.json({ success: true, message: "Restart initiated — service will restart in 2 seconds" });
      }

      if (action === "restore") {
        const last = await pool.query("SELECT * FROM deployment_runs WHERE approved=true ORDER BY started_at DESC LIMIT 1").catch(() => ({ rows: [] }));
        return res.json({ success: true, message: "Restore initiated", last_good_run: last.rows[0] || null });
      }

      return res.status(400).json({ success: false, error: `Unknown action: ${action}` });
    })
  );

  // ── GITHUB: Save Secrets ─────────────────────────────────────
  app.post("/deployment/github/secrets", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const { github_token, github_repo } = req.body;
      if (!github_token) return res.status(400).json({ success: false, error: "github_token is required" });

      // Store reference — actual secret stored in env (Replit Secrets)
      logger.info("GitHub credentials updated", { repo: github_repo, by: req.user.id });
      return res.json({
        success: true,
        message: "GitHub credentials received. Set GITHUB_TOKEN in Replit Secrets for persistence.",
        repo: github_repo,
        note: "Token is not stored in DB for security. Use Replit Secrets panel.",
      });
    })
  );

  // ── GITHUB: Push ─────────────────────────────────────────────
  app.post("/deployment/github/push", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const { commit_message = "feat: DUNAZOE Control Plane update", repo } = req.body;
      const github_token = process.env.GITHUB_TOKEN;

      if (!github_token) {
        return res.json({
          success: false,
          error: "GITHUB_TOKEN not set",
          manual_commands: [
            "git add .",
            `git commit -m "${commit_message}"`,
            "git push origin main",
          ],
          instructions: "Set GITHUB_TOKEN in Replit Secrets, then retry. Or run manual commands in terminal.",
        });
      }

      try {
        const gitRepo = repo || process.env.GITHUB_REPO || "dunazoeworld-stack/dunazoe-supermaster";
        execSync(`git config user.email "deployment-ai@dunazoe.com"`, { stdio: "pipe" });
        execSync(`git config user.name "DUNAZOE Deployment AI"`, { stdio: "pipe" });
        execSync(`git add .`, { cwd: process.cwd(), stdio: "pipe" });
        const commitOutput = execSync(`git commit -m "${commit_message}" || echo "nothing to commit"`, { cwd: process.cwd(), stdio: "pipe" }).toString();
        const remote = `https://${github_token}@github.com/${gitRepo}.git`;
        execSync(`git push ${remote} HEAD:main`, { cwd: process.cwd(), stdio: "pipe" });

        logger.info("GitHub push successful", { repo: gitRepo, by: req.user.id });
        return res.json({ success: true, message: `Pushed to ${gitRepo} — CI/CD triggered`, commit: commit_message });
      } catch (e) {
        return res.json({
          success: false,
          error: e.message.substring(0, 200),
          manual_commands: ["git add .", `git commit -m "${commit_message}"`, "git push origin main"],
        });
      }
    })
  );

  // ── GITHUB: Pull ─────────────────────────────────────────────
  app.post("/deployment/github/pull", requireAuth,
    requireRole("admin","cto","super_admin"),
    asyncHandler(async (req, res) => {
      const github_token = process.env.GITHUB_TOKEN;
      const gitRepo = process.env.GITHUB_REPO || "dunazoeworld-stack/dunazoe-supermaster";

      try {
        execSync(`git fetch origin`, { cwd: process.cwd(), stdio: "pipe" });
        const output = execSync(`git pull origin main`, { cwd: process.cwd(), stdio: "pipe" }).toString();
        logger.info("GitHub pull successful", { by: req.user.id });
        return res.json({ success: true, message: `Pulled latest from ${gitRepo}`, output: output.substring(0, 300) });
      } catch (e) {
        return res.json({
          success: false,
          error: e.message.substring(0, 200),
          manual_commands: ["git fetch origin", "git pull origin main"],
        });
      }
    })
  );

  // ── GITHUB: Enhanced Status ──────────────────────────────────
  app.get("/deployment/github", requireAuth,
    asyncHandler(async (req, res) => {
      let currentTag = "v1.0.0-rc1";
      let currentBranch = "main";
      let lastCommit = null;

      try {
        currentBranch = execSync("git rev-parse --abbrev-ref HEAD", { stdio: "pipe" }).toString().trim();
        currentTag    = execSync("git describe --tags --abbrev=0 2>/dev/null || echo v1.0.0-rc1", { stdio: "pipe" }).toString().trim();
        lastCommit    = execSync("git log -1 --format='%h %s' 2>/dev/null || echo 'no commits'", { stdio: "pipe" }).toString().trim();
      } catch (_) {}

      return res.json({
        success: true,
        repo:    process.env.GITHUB_REPO || "dunazoeworld-stack/dunazoe-supermaster",
        branch:  currentBranch,
        tag:     currentTag,
        last_commit: lastCommit,
        github_connected: !!(process.env.GITHUB_TOKEN),
        status:  "READY_TO_PUSH",
        last_checked: new Date().toISOString(),
        push_commands: [
          "git add .",
          `git commit -m "feat: DUNAZOE Control Plane $(date +%Y%m%d-%H%M)"`,
          `git tag ${currentTag}`,
          "git push origin main",
          `git push origin ${currentTag}`,
        ],
        ci_status: "active",
        ci_file: ".github/workflows/ci.yml",
      });
    })
  );

  logger.info("✅ Control Plane routes registered (Phases 1-9)");
};
